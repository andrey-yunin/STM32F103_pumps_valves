/*
 * device_mapping.h
 *
 * Маппинг логических device_id дирижера в физические индексы
 * для платы насосов и клапанов (Pump Board, CAN_ADDR=0x30).
 *
 * Диапазон device_id для Pump Board: 10-29
 *   Насосы:  10-22 (13 шт.)
 *   Клапаны: 23-25 (3 шт.)
 *
 *
 *  Created on: Mar 6, 2026
 *      Author: andrey
 */

#ifndef DEVICE_MAPPING_H_
#define DEVICE_MAPPING_H_

#include <stdint.h>

// ============================================================
// Логические ID устройств (из реестра дирижера)
// ============================================================

// --- Насосы, диапазон 0-12 ---
#define DEV_PUMP_0                  0  // Насос 0 (уточнить назначение)
#define DEV_PUMP_1                  1  // Насос слива станции промывки
#define DEV_PUMP_2                  2  // Насос 3 (уточнить назначение)
#define DEV_PUMP_3                  3  // Насос 4 (уточнить назначение)
#define DEV_PUMP_4                  4  // Насос 5 (уточнить назначение)
#define DEV_PUMP_5                  5  // Насос 6 (уточнить назначение)
#define DEV_PUMP_6                  6  // Насос 7 (уточнить назначение)
#define DEV_PUMP_7                  7  // Насос 8 (уточнить назначение)
#define DEV_PUMP_8                  8  // Насос 9 (уточнить назначение)
#define DEV_PUMP_9                  9  // Насос 10 (уточнить назначение)
#define DEV_PUMP_10                 10  // Насос 11 (уточнить назначение)
#define DEV_PUMP_11                 11  // Насос 12 (уточнить назначение)
#define DEV_PUMP_12                 12  // Насос 13 (уточнить назначение)

// --- Клапаны, диапазон 13-15 ---
#define DEV_VALVE_1                 13  // Клапан 1 (уточнить назначение)
#define DEV_VALVE_2                 14  // Клапан 2 (уточнить назначение)
#define DEV_VALVE_3                 15  // Клапан 3 (уточнить назначение)


// ============================================================
// Специальные значения
// ============================================================
#define DEVICE_ID_INVALID           0xFF

// ============================================================
// Типы устройств
// ============================================================
#define DEVICE_TYPE_PUMP            0
#define DEVICE_TYPE_VALVE           1
#define DEVICE_TYPE_UNKNOWN         0xFF

// ============================================================
// Результат маппинга
// ============================================================
typedef struct {
	uint8_t physical_id;    // Физический индекс (0-based) или DEVICE_ID_INVALID
    uint8_t device_type;    // DEVICE_TYPE_PUMP / DEVICE_TYPE_VALVE / DEVICE_TYPE_UNKNOWN
    } DeviceMappingResult_t;

// ============================================================
// Публичный API
// ============================================================
DeviceMappingResult_t DeviceMapping_Resolve(uint8_t device_id);


#endif /* DEVICE_MAPPING_H_ */
