/*
 * device_mapping.h
 *
 * Централизованный реестр идентификаторов всех физических устройств анализатора ДДС-240.
 * Единственный источник правды для motor_id, pump_id, photometer_id и т.д.
 *
 * Схема нумерации: подгруппы с шагом 10 (1-9 дозатор, 10-19 диски, 20-29 миксер, 30-39 фотометр).
 * Используются #define (не enum), т.к. поля в структурах имеют тип uint8_t.
 *
 *  Created on: Mar 2, 2026
 *      Author: andrey
 */

#ifndef INC_DISPATCHER_DEVICE_MAPPING_H_
#define INC_DISPATCHER_DEVICE_MAPPING_H_

// ============================================================================
// ---                       МОТОРЫ (Motor IDs)                             ---
// ============================================================================

// --- Дозатор (Dispenser), диапазон 1-9 ---
#define DEV_DISPENSER_MOTOR_XY       1   // Горизонтальное перемещение руки дозатора
#define DEV_DISPENSER_MOTOR_Z        2   // Вертикальное движение иглы
#define DEV_DISPENSER_MOTOR_SYRINGE  3   // Шприцевой мотор (забор/выдача жидкости)
// 4-9: резерв (2-й дозатор и т.д.)

// --- Диски и роторы, диапазон 10-19 ---
#define DEV_REACTION_DISK_MOTOR          10  // Вращение реакционного диска
#define DEV_REAGENT_SAMPLE_DISK_MOTOR    11  // Вращение диска реагентов/образцов (один физический диск)
// 12-19: резерв

// --- Миксер (Mixer), диапазон 20-29 ---
#define DEV_MIXER_MOTOR_XY           20  // Горизонтальное перемещение миксера
#define DEV_MIXER_MOTOR_Z            21  // Подъём/опускание лопатки
#define DEV_MIXER_PADDLE_MOTOR       22  // Вращение лопатки (перемешивание)
// 23-29: резерв (2-й миксер и т.д.)

// --- Фотометр, диапазон 30-39 ---
// У фотометра нет собственного мотора, диапазон зарезервирован.
// 30-39: резерв

// ============================================================================
// ---                       НАСОСЫ (Pump IDs)                              ---
// ============================================================================

// --- Специальное значение ---
#define DEV_PUMP_DYNAMIC             0   // Заглушка, заменяется в runtime из параметров команды

// --- Станция промывки (Wash Station), диапазон 10-19 ---
#define DEV_WASH_PUMP_FILL           10  // Насос заполнения
#define DEV_WASH_PUMP_DRAIN          11  // Насос слива
// 12-19: резерв

// ============================================================================
// ---                     ФОТОМЕТР (Photometer IDs)                        ---
// ============================================================================

#define DEV_PHOTOMETER_MAIN          1   // Основной фотометр

#endif /* INC_DISPATCHER_DEVICE_MAPPING_H_ */
